public class lab043 {

    public static void main(String[] args) {


        //Decrement Operator
        int b=13;
        System.out.println(--b + b--);
        //part A: (Exp=12 , b=12 ) --b   --> o/p:12
        //Part B: (exp=12 , b= 11 ) b--  --> o/p:12
        //part A+part B =12+12=24
        System.out.println(b);

        System.out.println(--b - b--);
        //part A- part B =12-12=0





        







    }
}
