package src.basics_03;

public class lab039 {

    public static void main(String[] args) {


        //Increment Operator

        int a=10;
        a= a+1;
        System.out.println(a);

        //OR//

        a+=1;
        System.out.println(a);

        //OR//

        a++;            //Post increment        a++--> a=a+1
        System.out.println(a);

        //OR//

        ++a;            //pre increment     ++a---> a=a+1
        System.out.println(a);





        







    }
}
